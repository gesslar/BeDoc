/**
 * @file WikiText Printer - A printer for generating WikiText formatted documentation.
 *
 * This printer transforms structured documentation data into WikiText/MediaWiki
 * format, suitable for wiki platforms. It handles formatting of function
 * signatures, parameters, return values, and examples using wiki markup syntax.
 *
 * The printer uses a contract-based approach defined in bedoc-wikitext-printer.yaml
 * and integrates with the BeDoc documentation system through ActionBuilder.
 * @author gesslar
 * @version 1.0.0
 * @since 1.0.0
 */

import { ACTIVITY } from "@gesslar/actioneer"
import { Data, Term } from "@gesslar/toolkit"

const {UNTIL} = ACTIVITY

/** @typedef {import("@gesslar/actioneer").ActionBuilder} ActionBuilder */

/**
 * WikiText Printer Class - Formats documentation as WikiText markup.
 *
 * This printer takes structured function documentation and outputs it in
 * WikiText/MediaWiki format with proper markup for headings, code blocks,
 * parameters, and other documentation elements.
 * @class
 */
export default class {
  /**
   * Printer metadata defining its characteristics and contract.
   * @type {object}
   * @readonly
   * @property {string} kind - The type of action ("print")
   * @property {string} output - The output format this printer generates ("wikitext")
   * @property {string} terms - References the YAML contract file that defines the input schema
   */
  static meta = Object.freeze({
    kind: "print",
    output: "wikitext",
    terms: "ref://./bedoc-wikitext-printer.yaml"
  })

  /**
   * Configures the printer using ActionBuilder's fluent API.
   *
   * This method sets up the printing pipeline with steps for preparing,
   * rendering, and finalizing WikiText output.
   * @param {ActionBuilder} ab - The ActionBuilder instance to configure
   * @returns {ActionBuilder} The configured builder instance
   */
  setup = ab => ab
    .do("Prepare functions", this.#prepare)
    .do("Render function", UNTIL, this.#hasMoreFunctions, this.#renderFunction)
    .do("Finalize output", this.#finalise)

  /**
   * Prepares the functions for rendering by sorting them.
   * @param {object} context - The context containing module name and functions
   * @param {string} context.moduleName - The name of the module being documented
   * @param {Array} context.functions - The array of function objects to be documented
   * @returns {Promise<object>} Prepared context with sorted functions
   * @private
   */
  async #prepare({moduleName, functions}) {
    const ordered = Data.typeOf(functions) === "Array"
      ? [...functions].sort((a, b) => {
        if(!a?.name || !b?.name)
          return 0

        return a.name.localeCompare(b.name)
      })
      : []

    return {
      moduleName,
      remaining: ordered,
      rendered: []
    }
  }

  /**
   * Predicate to check if there are more functions to render
   * @param {object} curr - Current context
   * @returns {boolean} True if more functions remain
   * @private
   */
  #hasMoreFunctions = curr => curr.remaining.length > 0

  /**
   * Renders a single function as WikiText markup.
   * @param {object} curr - Current rendering context
   * @returns {Promise<object>} Updated context with rendered function
   * @private
   */
  async #renderFunction(curr) {
    const {remaining, rendered} = curr
    const next = remaining.shift()

    if(!next)
      return curr

    const lines = []

    // Render function name as heading
    if(next.signature?.name)
      lines.push(`== ${next.signature.name} ==`)

    // Render deprecation warning if present
    if(Array.isArray(next.deprecated) && next.deprecated.length > 0) {
      const deprecatedText = next.deprecated.map(c => c?.trim()).filter(Boolean).join(" ")
      lines.push("", `{{Admonition|type=stop|title=Deprecated|${deprecatedText}}}`)
    }

    // Render function signature
    if(next.signature) {
      const sig = next.signature
      const modifiers = sig.modifiers?.length ? sig.modifiers.join(" ") + " " : ""
      const access = sig.access || "public"
      const type = sig.type || "void"
      const params = sig.parameters?.join(", ") || ""

      lines.push("", `<code>${access} ${modifiers}''${type}'' '''${sig.name}'''(${params})</code>`)
    }

    // Render description
    if(next.description) {
      const desc = next.description.map(line => line?.trim()).filter(Boolean).join("\n")
      if(desc)
        lines.push("", desc)
    }

    // Render parameters
    if(Array.isArray(next.param) && next.param.length > 0) {
      const paramLines = next.param.map(param => {
        // Parse parameter name for optional/default values
        let paramName = param.name
        let optionalParam = false
        let defaultValue = null

        // Check for optional parameter [name]
        const optionalMatch = paramName?.match(/^\[(.+)\]$/)
        if(optionalMatch) {
          optionalParam = true
          paramName = optionalMatch[1]
        }

        // Check for default value name=value
        const defaultMatch = paramName?.match(/^(.+)=(.+)$/)
        if(defaultMatch) {
          paramName = defaultMatch[1]
          defaultValue = defaultMatch[2]
        }

        // Build optional/default text
        let optionalText = ""
        if(optionalParam && defaultValue) {
          optionalText = ` (Optional. Default: ${defaultValue})`
        } else if(optionalParam) {
          optionalText = " (Optional)"
        } else if(defaultValue) {
          optionalText = ` (Default: ${defaultValue})`
        }

        // Format type
        const type = Array.isArray(param.type) ? param.type.join(" | ") : param.type

        // Format content
        const content = Array.isArray(param.content)
          ? param.content.map(c => c?.trim()).filter(Boolean).join(" ")
          : ""

        return `;'''${paramName}''' ''${type}${optionalText}''\n:${content}`
      }).filter(Boolean)

      if(paramLines.length) {
        lines.push("", ...paramLines)
      }
    }

    // Render return value
    if(next.return) {
      const type = Array.isArray(next.return.type)
        ? next.return.type.join(" | ")
        : next.return.type
      const content = Array.isArray(next.return.content)
        ? next.return.content.map(c => c?.trim()).filter(Boolean).join(" ")
        : ""

      if(type) {
        lines.push("", "=== Returns ===", "", `''<code>${type}</code>'' ${content}`.trim())
      }
    }

    // Render examples
    if(Array.isArray(next.example) && next.example.length > 0) {
      const examples = next.example.filter(Boolean).join("\n")
      if(examples)
        lines.push("", "=== Example ===", "", examples)
    }

    const result = lines.join("\n").trim()
    rendered.push(Data.appendString(result, "\n"))

    return curr
  }

  /**
   * Finalizes the output and creates the destination file info.
   * @param {object} curr - Current context with rendered functions
   * @returns {Promise<object>} Final output with file name and content
   * @private
   */
  async #finalise(curr) {
    const {moduleName, rendered} = curr

    if(!rendered.length)
      Term.warn(`No functions to print for module: \`${moduleName}\``)

    return {
      destFile: `${moduleName}.txt`,
      destContent: rendered.join("\n")
    }
  }
}
