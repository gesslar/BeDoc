/**
 * @file Lua Parser - A parser for extracting documentation from Lua files.
 *
 * This parser handles Lua function documentation using LDoc/LuaDoc-style
 * triple-dash comments (---) and extracts structured information including
 * descriptions, parameters, return types, and examples.
 *
 * The parser uses a contract-based approach defined in bedoc-lua-parser.yaml
 * and integrates with the BeDoc documentation system through ActionBuilder.
 * @author gesslar
 * @version 1.0.0
 * @since 1.0.0
 */

import { ActionBuilder, ActionRunner, ACTIVITY } from "@gesslar/actioneer"
import { Collection, Data } from "@gesslar/toolkit"

const {WHILE} = ACTIVITY

/** @typedef {import("@gesslar/actioneer").ActionBuilder} ActionBuilder */

/**
 * Lua Parser Class - Parses Lua files to extract function documentation.
 *
 * This parser is designed to work with Lua source files, extracting
 * LDoc/LuaDoc-style comments (---) and function signatures. It identifies
 * functions with their scope, parameters, and associated documentation.
 * @class
 */
export default class {
  /**
   * Parser metadata defining its characteristics and contract.
   * @type {object}
   * @readonly
   * @property {string} kind - The type of parser ("parse")
   * @property {string} input - The input file type this parser handles ("lua")
   * @property {object} contract - References the YAML contract file that defines the output schema
   */
  static meta = Object.freeze({
    kind: "parse",
    input: "lua",
    terms: "ref://./bedoc-lua-parser.yaml"
  })

  /**
   * Configures the parser using ActionBuilder's fluent API.
   *
   * This method sets up the parsing structure and extraction methods for Lua
   * documentation. It defines:
   * - Comment block structure (LDoc-style --- comments)
   * - Function signature patterns with Lua-specific syntax
   * - Extraction methods for descriptions, tags, and return values
   * @param {ActionBuilder} ab - The ActionBuilder instance to configure
   * @returns {ActionBuilder} The configured builder instance
   */
  setup = ab => ab
    .do("Extract function blocks", this.#extractBlocks)
    .do("Process blocks", this.#parallelExtraction)
    .do("Finalize", this.#finally)

  #parallelExtraction = async value => {
    const builder = new ActionBuilder(this)
      .do("Extract description", this.#extractDescription)
      .do("Extract tags", WHILE, this.#hasMoreTags, this.#extractTag)
      .do("Extract return", this.#extractReturn)
      .build()

    const runner = new ActionRunner(builder)
    const result = await runner.pipe(value.blocks)

    return result
  }

  async #extractBlocks(value) {
    const {content} = value
    const result = []
    const lines = Data.appendString(content, "\n").split("\n")

    while(lines.length) {
      const block = {}

      // Find the block start index (first --- comment)
      const startIndex = lines.findIndex(line => this.#regexes.get("block-start").test(line))

      if(startIndex < 0)
        break

      // Collect all consecutive --- comment lines
      const commentLines = []
      let currentIndex = startIndex

      while(currentIndex < lines.length && this.#regexes.get("comment-line").test(lines[currentIndex])) {
        const match = this.#regexes.get("comment-line").exec(lines[currentIndex])
        if(match?.groups?.content !== undefined) {
          commentLines.push(match.groups.content)
        }
        currentIndex++
      }

      block.lines = commentLines

      // Remove processed comment lines
      lines.splice(0, currentIndex)

      // Find the function signature
      const funcIndex = lines.findIndex(line => this.#regexes.get("function").test(line))

      if(funcIndex > -1) {
        // Check if there's another comment block before this function
        const nextBlockIndex = lines.findIndex(line => this.#regexes.get("block-start").test(line))

        if(nextBlockIndex !== -1 && funcIndex > nextBlockIndex) {
          // Function is after next block, skip this block
          lines.splice(0, nextBlockIndex)
        } else {
          // Found the function for this block
          const func = this.#regexes.get("function").exec(lines[funcIndex])
          block.function = func
          lines.splice(0, funcIndex + 1)
        }
      }

      result.push(block)
    }

    Object.assign(value, {
      blocks: result,
      file: value.file
    })

    return value
  }

  /**
   * Extracts the description section from LDoc-style comment lines.
   *
   * Processes comment lines to extract the main description text that appears
   * before any \@tag declarations.
   * @param {object} value - The block object containing comment lines to process
   * @returns {Promise<Array<string> | null>} Array of description lines, or null if empty
   * @private
   * @async
   */
  #extractDescription = async value => {
    const {lines} = value

    const description = []

    while(lines.length > 0) {
      const line = lines[0]

      // Stop if we hit a tag
      if(this.#regexes.get("tag-start").test(line))
        break

      description.push(lines.shift())
    }

    Object.assign(value, {description: description.filter(Boolean)})

    return value
  }

  /**
   * Predicate to check if there are more tags to extract
   * @param {object} value - Current context
   * @returns {boolean} True if more tags exist
   * @private
   */
  #hasMoreTags = async value => {
    const text = value.lines.join("\n")
    const tagTest = this.#regexes.get("tag")
    const returnExcepted = this.#regexes.get("tag-except")
    return tagTest.test(text) && !returnExcepted.some(re => re.test(text))
  }

  /**
   * Extracts LDoc-style tags from comment lines.
   * @param {object} value - The block object containing comment lines to process
   * @returns {Promise<object>} Updated value object with extracted tags
   * @private
   * @async
   */
  #extractTag = async (value) => {
    const {lines, tag = []} = value
    const result = {}
    const text = lines.join("\n")
    const tagTest = this.#regexes.get("tag")

    if(!tagTest.test(text))
      return value

    const firstLine = lines.shift()
    const tagMatch = this.#regexes.get("tag").exec(firstLine)

    if(!tagMatch)
      return value

    const {matchedTag, name, type, content} = tagMatch.groups

    // Collect multiline content
    const contentLines = [content].filter(Boolean)

    while(lines.length > 0 && !this.#regexes.get("tag-start").test(lines[0])) {
      contentLines.push(lines.shift())
    }

    result[matchedTag] = {
      name,
      type: type ? type.split(",").map(t => t.trim()) : [],
      content: contentLines
    }

    tag.push(result)
    Object.assign(value, {tag})

    return value
  }

  /**
   * Extracts \@return tag from comment lines.
   * @param {object} value - The block object containing comment lines to process
   * @returns {Promise<object>} Updated value object with return information
   * @private
   * @async
   */
  #extractReturn = async value => {
    const {lines, tag = []} = value
    const result = {}
    const text = lines.join("\n")
    const tagTest = this.#regexes.get("return")

    if(!tagTest.test(text))
      return value

    const firstLine = lines.shift()
    const tagMatch = this.#regexes.get("return").exec(firstLine)

    if(!tagMatch)
      return value

    const {type, content} = tagMatch.groups

    // Collect multiline content
    const contentLines = [content].filter(Boolean)

    while(lines.length > 0 && !this.#regexes.get("tag-start").test(lines[0])) {
      contentLines.push(lines.shift())
    }

    result.return = {
      type: type ? type.split(",").map(t => t.trim()) : [],
      content: contentLines
    }

    tag.push(result)
    Object.assign(value, {tag})

    return value
  }

  /**
   * Final processing method called after all extraction is complete.
   * @param {Array<object>} value - Array of extracted blocks
   * @returns {Promise<object>} The transformation results
   * @private
   */
  async #finally(value) {
    const result = await Collection.asyncMap(value, async item => {
      const result = {}
      result.name = item.function?.groups?.name

      result.description = item.description || []

      const tags = item.tag || []

      if(tags.length > 0) {
        const combined = Collection.flattenObjectArray(tags)

        if(combined.param) {
          result.param = combined.param.map(p => {
            const {type, name, content} = p
            return {type, name, content}
          })
        }

        if(combined.return) {
          const {type, content} = combined.return[0]
          result.return = {type, content}
        }

        if(combined.example) {
          result.example = combined.example.flatMap(e => e.content)
        }
      }

      return result
    })

    return {functions: result}
  }

  /**
   * Regular expressions used for parsing Lua documentation blocks and function signatures.
   *
   * This map contains patterns for:
   * - Block start detection (--- comments)
   * - Description and tag extraction
   * - Function signature parsing
   * - LDoc tag identification
   * @type {Map<string, RegExp | RegExp[]>}
   * @private
   */
  #regexes = new Map([
    ["block-start", /^\s*---\s?(.*)$/],
    ["comment-line", /^\s*---\s?(?<content>.*)$/],
    ["tag-start", /^@/],
    ["tag", /^@(?<matchedTag>param)\s+(?<name>\S+)\s+(?<type>\S+)\s+#\s+(?<content>.*)$/],
    ["tag-except", [/^@return/]],
    ["return", /^@return\s+(?<type>\S+)\s+#\s+(?<content>.*)?$/],
    ["function", /^\s*function\s+(?<name>(?<scope>[a-zA-Z_]\w*(?=[.:]))?(?<delimiter>[.:])?(?<method>[a-zA-Z_]\w*))\s*\((?<parms>.+)?\)\s*(?:end)?$/]
  ])
}
